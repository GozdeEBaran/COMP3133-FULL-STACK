// Calculator functions for basic arithmetic operations

// Addition function
const add = (i, j) => {
  return i + j;
};

// Multiplication function
const mul = (i, j) => {
  return i * j;
};

// Division function
const div = (i, j) => {
  return i / j;
};

// Subtraction function
const sub = (i, j) => {
  return i - j;
};

// Export all calculator functions
module.exports = {
  add,
  mul,
  div,
  sub,
};
